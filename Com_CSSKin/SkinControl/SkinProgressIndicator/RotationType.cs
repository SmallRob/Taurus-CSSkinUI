
namespace Com_CSSkin.SkinControl
{
    /// <summary>
    /// 枚举用于指示旋转方向的控制。
    /// </summary>
    public enum RotationType
    {
        /// <summary>
        /// 指出应顺时针旋转。
        /// </summary>
        Clockwise = 1,
        /// <summary>
        /// 表明要逆时针旋转。
        /// </summary>
        CounterClockwise = -1,
    }
}